package com.back.model;

public class Recruit {
	private int ID;
	private String FirmName;
	private String Job;
	private String Department;
	private String Pay;
	private String Welfare;
	private String EducationBgd;
	private String JobNature;
	private String Job_Des;
	private String Date;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getFirmName() {
		return FirmName;
	}
	public void setFirmName(String firmName) {
		FirmName = firmName;
	}
	public String getJob() {
		return Job;
	}
	public void setJob(String job) {
		Job = job;
	}
	public String getDepartment() {
		return Department;
	}
	public void setDepartment(String department) {
		Department = department;
	}
	public String getPay() {
		return Pay;
	}
	public void setPay(String pay) {
		Pay = pay;
	}
	public String getWelfare() {
		return Welfare;
	}
	public void setWelfare(String welfare) {
		Welfare = welfare;
	}
	public String getEducationBgd() {
		return EducationBgd;
	}
	public void setEducationBgd(String educationBgd) {
		EducationBgd = educationBgd;
	}
	public String getJobNature() {
		return JobNature;
	}
	public void setJobNature(String jobNature) {
		JobNature = jobNature;
	}
	public String getJob_Des() {
		return Job_Des;
	}
	public void setJob_Des(String job_Des) {
		Job_Des = job_Des;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
	
	

}
